package javabasic;

public class SumOfTwoElement {
	public static void main(String[] args) {
		int a=20;
		int b=30;
		int c=a+b;
		System.out.println("sum is :"+c);
		int d=a-b;
		System.out.println("diff is :"+d);
		float e=b/a;
		System.out.println("div is :"+e);
		int f=a*b;
		System.out.println("mul is :"+f);
	}
}
